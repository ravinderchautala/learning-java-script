const dice = 2;

switch (dice) {
    case 1: console.log("you get one");
        break;
    case 2: console.log("you get two");
        break;
    case 3: console.log("you get three");
        break;
    case 4: console.log("you get four");
        break;
    case 5: console.log("you get five");
        break;
    case 6: console.log("you get six");
        break;
    default: console.log("you didn't roll dice properly");
}