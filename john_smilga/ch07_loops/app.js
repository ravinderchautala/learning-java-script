// while loop
let amount = 10;
while (amount > 0) {
    console.log('I have ' + amount + '$');
    amount--;
}

// do while loop
let money = 0
do {
    console.log('You have ' + money + ' dollars')
    money++
} while (money < 10)

//  for loop
for (let i = 0; i < 10; i++) {
    console.log("Vinita")
}